/// Represents the size of a video.
///
/// A [VideoSize] object contains the width and height of a video in pixels.
class VideoSize {
  /// The width of the video in pixels.
  final int width;

  /// The height of the video in pixels.
  final int height;

  /// Creates a new [VideoSize] object.
  ///
  /// [width] and [height] are the width and height of the video in pixels.
  VideoSize(this.width, this.height);
}
